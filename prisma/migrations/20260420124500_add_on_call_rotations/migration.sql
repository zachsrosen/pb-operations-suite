-- On-Call Electrician Rotations (V1 admin-managed)
-- Spec: docs/superpowers/specs/2026-04-20-on-call-electrician-rotations-design.md
-- Plan: docs/superpowers/plans/2026-04-20-on-call-electrician-rotations.md
